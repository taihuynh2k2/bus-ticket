-- Bước 1: Xóa cơ sở dữ liệu nếu đã tồn tại (CHỈ KHI BẠN MUỐN BẮT ĐẦU HOÀN TOÀN MỚI)
DROP DATABASE IF EXISTS bus_ticket_db;


-- Create Database
CREATE DATABASE IF NOT EXISTS bus_ticket_db;
USE bus_ticket_db;

-- Tạo bảng người dùng (Users)
CREATE TABLE users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL, -- Lưu trữ mật khẩu đã băm
    email VARCHAR(100) NOT NULL UNIQUE,
    full_name VARCHAR(100),
    phone_number VARCHAR(20),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tạo bảng xe buýt (Buses)
CREATE TABLE buses (
    id INT AUTO_INCREMENT PRIMARY KEY,
    bus_number VARCHAR(20) NOT NULL UNIQUE,
    capacity INT NOT NULL, -- Sức chứa của xe
    model VARCHAR(50),
    status VARCHAR(20) DEFAULT 'active' -- Ví dụ: active, maintenance
);

-- Tạo bảng tuyến đường (Routes)
CREATE TABLE routes (
    id INT AUTO_INCREMENT PRIMARY KEY,
    origin VARCHAR(100) NOT NULL,
    destination VARCHAR(100) NOT NULL,
    distance DECIMAL(10, 2), -- Khoảng cách tuyến đường (nếu cần)
    estimated_duration VARCHAR(50) -- Thời gian dự kiến của chuyến đi
);

-- Tạo bảng lịch trình (Schedules)
-- Mỗi lịch trình là một chuyến xe buýt cụ thể trên một tuyến đường vào một thời gian nhất định
CREATE TABLE schedules (
    id INT AUTO_INCREMENT PRIMARY KEY,
    route_id INT NOT NULL,
    bus_id INT NOT NULL,
    departure_time DATETIME NOT NULL,
    arrival_time DATETIME NOT NULL,
    price DECIMAL(10, 2) NOT NULL,
    available_seats INT NOT NULL, -- Số ghế còn trống (có thể được cập nhật khi đặt vé)
    total_seats INT NOT NULL, -- Tổng số ghế của xe buýt này trong lịch trình này
    status VARCHAR(20) DEFAULT 'scheduled', -- Ví dụ: scheduled, departed, arrived, cancelled
    FOREIGN KEY (route_id) REFERENCES routes(id) ON DELETE CASCADE,
    FOREIGN KEY (bus_id) REFERENCES buses(id) ON DELETE CASCADE
);

-- Tạo bảng đặt vé (Bookings)
CREATE TABLE bookings (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    schedule_id INT NOT NULL,
    booking_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    total_amount DECIMAL(10, 2) NOT NULL,
    status VARCHAR(20) DEFAULT 'pending', -- Ví dụ: pending, confirmed, cancelled, refunded
    payment_method VARCHAR(50), -- Ví dụ: cash, credit_card, momo
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY (schedule_id) REFERENCES schedules(id) ON DELETE CASCADE
);

-- Tạo bảng chi tiết vé (Tickets)
-- Mỗi vé là một ghế cụ thể trong một đặt vé
CREATE TABLE tickets (
    id INT AUTO_INCREMENT PRIMARY KEY,
    booking_id INT NOT NULL,
    seat_number VARCHAR(10) NOT NULL,
    passenger_name VARCHAR(100) NOT NULL,
    passenger_phone VARCHAR(20),
    status VARCHAR(20) DEFAULT 'active', -- Ví dụ: active, used, cancelled, refunded
    FOREIGN KEY (booking_id) REFERENCES bookings(id) ON DELETE CASCADE
);

-- Bảng để lưu trữ trạng thái ghế của từng chuyến (có thể mở rộng cho phức tạp hơn)
-- Mở rộng để lưu trạng thái ghế cụ thể cho từng lịch trình
CREATE TABLE seat_status (
    id INT AUTO_INCREMENT PRIMARY KEY,
    schedule_id INT NOT NULL,
    seat_number VARCHAR(10) NOT NULL,
    is_booked BOOLEAN DEFAULT FALSE,
    FOREIGN KEY (schedule_id) REFERENCES schedules(id) ON DELETE CASCADE,
    UNIQUE (schedule_id, seat_number) -- Đảm bảo mỗi ghế trong một chuyến chỉ có một trạng thái
);

